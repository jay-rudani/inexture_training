package Operators.Arithmetic;

class ArithmeticOps {

    public static void main(String[] args) {

        int x = 5;
        System.out.println("Addition : 10 + 10 : " + (10 + 10));
        System.out.println("Subtraction : 20 - 10 : " + (20 - 10));
        System.out.println("Multiplication : 2 * 3 : " + (2 * 3));
        System.out.println("Division : 12 / 3 : " + (12 / 3));
        System.out.println("Remainder : 5 % 4 : " + (5 % 4));
        System.out.println("increment : " + (++x));
        System.out.println("Decrement : " + (--x));

    }
}
